#include <iostream>
using namespace std;

int main() {
    // number declaration
    short   s;
    int     i;
    long    l;
    float   f;
    double  d;

    // number assignments
    s = 10;
    i = 1000;
    l = 10000000;
    f = 230.47;
    d = 30949.347;

    // number printing
    cout << "short  s: " << s << endl;
    cout << "int    i: " << i << endl;
    cout << "long   l: " << l << endl;
    cout << "float  f: " << f << endl;
    cout << "double d: " << d << endl;

    return 0;
}
